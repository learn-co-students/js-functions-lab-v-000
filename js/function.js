function happyHolidays () {
  return "Happy Holidays!!";
};

function happyHolidaysWithName (name) {
  return "Happy Holidays, " + name;
};

function happyAnyHoliday (holiday_name, name) {
  return "Happy " + holiday_name + ", " + name;
};

function holidayCountDown (holiday_name, days_until) {
  return "It's " + days_until.toString() + " till " + holiday_name;
};
