function happyHolidays() {
  return "Happy Holidays!!";
}

function happyHolidaysWithName(name) {
  return "Happy Holidays, " + name;
}

function happyAnyHoliday(personName, holiday) {
  return "Happy " + holiday + ", " + personName;
}

function holidayCountDown(holiday, numDays) {
  return "It's " + numDays + " days till " + holiday + "!";
}