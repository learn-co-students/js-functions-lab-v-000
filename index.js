function happyHolidays(){
  return "Happy holidays!"
}

function happyHolidaysTo(name){
  return `Happy holidays, ${name}!`
}


function happyHolidayTo(season, name){
  return `Happy ${season}, ${name}!`
}

function holidayCountdown(season, number){
  return `It's ${number} days until ${season}!`
}
